<p>Michael Baxandall - Pintura y Vida Cotidiana en el Renacimiento - (p.60)</p>

<h3>El cuadro debe narrar su asunto en una forma clara para los simples, en una forma vistosa y memorable para los olvidadizos y haciendo pleno uso de
 todos los recursos emocionales del sentido de la vista, el más poderoso y también el más preciso de los sentidos.</h3>
<p>Michael Baxandall - Pintura y Vida Cotidiana en el Renacimiento - (p.62)</p>

