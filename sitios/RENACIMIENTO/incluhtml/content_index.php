<h3>El pensamiento humanista contempla al hombre como ser divinizado. La distancia entre las esferas de lo divino y lo humano se reducen
 considerablemente; la imagen de Dios - especialmente a través de la imagen de Cristo - se humaniza y la imagen del hombre adquiere rasgos de divinización.</h3>  
<h3>El objetivo es reabsorber los contenidos filosóficos, morales y artísticos de las civilizaciones griega y romana. El anhelo del pasado no es un 
	sentimiento conservador, sino que entraña una poderosa voluntad de futuro.</h3>
    <p>Rafael Argullol - Tres Miradas Sobre El Arte (p.56)</p>

<h3>Pintura religiosa: significa que los cuadros existían para atender finalidades institucionales. Lo cuadros caían bajo la jurisdicción de un corpus 
