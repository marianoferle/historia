<p>Jacques Le Gorff y Nicolas Truong - Una historia del cuerpo en la Edad Media (p.29)</p>

<h3>La abstinencia y el ayuno marcan el ritmo del hombre medieval. El dominio del cuerpo se acompaña con el dominio del tiempoLa abstinencia y el ayuno 
	marcan el ritmo del hombre medieval. El dominio del cuerpo se acompaña con el dominio del tiempo</h3>
<p>Jacques Le Gorff y Nicolas Truong - Una historia del cuerpo en la Edad Media</p>

<h3>La vida cotidiana de los hombres de la Edad Media oscila entre Cuaresma y Carnaval. De un lado, el clero reprime las practicas corporales, del otro
 las glorifica. De un lado, la Cuaresma se abate sobre la vida cotidiana del hombre medieval, del otro el Carnaval retoza en sus exesos. Sexualidad, trabajo, 
 sueño, vestimenta, guerra, gesto, risa.. el cuerpo es en la Edad Media una fuente de debates.</h3>
<p>Jacques Le Gorff y Nicolas Truong - Una historia del cuerpo en la Edad Media (p.31)</p>

<h3>Cuaresma: Cuarenta d&iacute;as de penitencia y de ayuno que, desde su fusi&oacute;n en el siglo IV, preceden y preparan la fiesta de Pascua, y que luego se extiende a 
	Navidad y Pentecost&eacute;s.</h3>
<p>Jacques Le Gorff y Nicolas Truong - Una historia del cuerpo en la Edad Media (p.52)</p>

<h3>Carnaval:  El martes de Carnaval o graso, es el d&iacute;a que precede al mi&eacute;rcoles de Ceniza que inaugura el periodo de ayuno. Las fiestas de Carnaval que come,
 con sus bailes, los caroles, considerados obsesos por el clero, se oponen a la Cuaresma que ayuna. </h3>
<p>Jacques Le Gorff y Nicolas Truong - Una historia del cuerpo en la Edad Media (p.52)</p>

