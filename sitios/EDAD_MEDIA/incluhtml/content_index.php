<h3>En la Edad Media el cuerpo se convierte en una met&aacute;fora. Estas se articulan principalmente en la Antigüedad en torno a un sistema caput-venter-membra 
	(cabeza-entrañas-miembros). </h3>
<h3>
<span class="coll">El pecho (pectus) y el coraz&oacute;n (cor):</span> ser&aacute;n sede del pensamiento y de los sentimientos.
</br>
<span class="coll">El h&iacute;gado (jecur o jocur):</span> &oacute;rgano sagrado y a continuaci&oacute;n en la sede de las pasiones.
</br>
<span class="coll">El vientre:</span> desempeña en el cuerpo el papel de coordinaci&oacute;n y al que los miembros deben obedecer, ya que transforma la alimentaci&oacute;n en sangre.
</br>
<span class="coll">Las manos:</span> representativo de las tensiones ideol&oacute;gicas y sociales del periodo. es signo de la protecci&oacute;n y del mando, pero tambi&eacute;n es el instrumento de la 
penitencia, del trabajo inferior.
</br>
<span class="coll">La boca:</span> con el beso simb&oacute;lico de la paz. (un beso en la boca). s&iacute;mbolo del amor cortes entre el caballero y su dama.
</h3>
<p>Michael Pastoureau - Una historia simbolica de la Edad Media occidental.</p>

<h3>EL uso simb&oacute;lico del cuerpo sirve para reforzar el poder de los dos h&eacute;roes de la Edad Media: el rey y el santo.</h3>
<p>Michael Pastoureau - Una historia simbolica de la Edad Media occidental.</p>

