<div id="titulo"><h1>EDAD MEDIA</h1><h2>El S&iacute;mbolo</h2><p>Mariano Ferle - IUNA - HSA II - 2012</p></div>
<div id="img2"></div>
<div class="texto">

<h3>En la cultura medieval, el s&iacute;mbolo constituye el primer utillaje mental. Siempre es ambiguo, polivalente, multiforme; no se traduce solo por palabras o per
 textos, sino tambi&eacute;n por im&aacute;genes, objetos, gestos, rituales, creencias, comportamientos. </h3>
<p>Michael Pastoureau - Una historia simbolica de la Edad Media occidental. ( p.12)</p>

<h3>El s&iacute;mbolo tiene como significado una entidad abstracta, una idea, una noci&oacute;n, un concepto. </h3>
<p>Michael Pastoureau - Una historia simbolica de la Edad Media occidental. ( p.13)</p>

<h3>El s&iacute;mbolo medieval casi siempre se constituye en torno de una relaci&oacute;n de tipo anal&oacute;gica, basada en el parecido entre dos palabras, dos nociones, dos objetos, 
	o bien en correspondencia entre una cosa y una idea.</h3>
<p>Michael Pastoureau - Una historia simbolica de la Edad Media occidental. ( p.17)</p>

<h3>Una palabra, una forma, un color, una materia, un numero, un gesto, un animal, un vegetal e incluso una persona pueden estar investidos de una funci&oacute;n simb&oacute;lica 
	y de ese modo, evocar, representar o significar algo diferente de lo que pretenden ser o mostrar</h3>
<p>Michael Pastoureau - Una historia simbolica de la Edad Media occidental. ( p.18)</p>

