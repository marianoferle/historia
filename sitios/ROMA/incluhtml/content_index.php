<div id="con_mat" class="img_to"><div class="text2">
<h1>Ara Pacis Agustae</h1></br></br>
Altar de la paz del emperador Augusto, primer emperador del Imperio Romano. Gobernó entre 27 a.c -14 d.c. / Mármol, 11 X 10 m / 13-9 a.c </br></br>
Monumento compuesto por un altar dentro de un recinto decorado con relieves figurados y ornamentales.</br></br>
 Se encontraba en la via Flaminia (el Corso) a la altura de la via in Lucina. / Descubiertas en parte en el Siglo XVI, se recuperaron por completo en 1937-38 y se recompusieron cerca del Mausoleo de Augusto. 
</div>
</div>

<div id="con_trans" class="img_to"><div class="text2">
<h1>Dimensi&oacute;n Transitiva</h1></br></br>
El inter&eacute;s del monumento est&aacute; en la decoraci&oacute;n del mismo, en su interior, la parte superior se decora con guirnaldas sostenidas por cráneos de bueyes y por pateras.</br></br>
Su interior en cambio est&aacute; compuesto por elementos totalmente independientes estructuralmente, solo unidos simb&oacute;licamente.
</br></br>En la entrada nos encontramos con dos escenas. Un guerrero romano observa a un hombre al otro extremo y en medio una loba amamanta a dos niños. en el otro extremo, un hombre observa a dos j&oacute;venes. Junto a ellos, se encuentran animales de granja.
</br></br>En la otra abertura del monumento, por un lado. En medio y en mayor tamaño  una mujer sosteniendo dos niños y por debajo, animales de granja;  En los extremos  la acompañan 2 mujeres sentadas, una sobre un ave que vuela en un fondo de cultivos, la otra sobre un monstro marino y fondo de aguas. La otra escena est&aacute; compuesta por una mujer sentada sobre un mont&oacute;n de armas. 
</br></br>Los dos lados largos del recinto est&aacute;n ocupados por dos hileras de figuras en procesi&oacute;n tomadas de la realidad, pero fijadas con gestos convencionales;  Entre las composiciones de los lados pequeños y los grandes  no existe relaci&oacute;n, al igual que el altar que ocupa casi todo el espacio en el interior del recinto. 
</br></br>El altar constaba de un plinto adornado con relieves y un friso con tema ritual (pequeñas figuras esculpidas en el m&aacute;rmol con vivacidad).
</div>
</div>

<div id="con_refle" class="img_to"><div class="text3">
<h1>Dimensi&oacute;n Reflexiva</h1><p>Bianchi Bandinelli, Ranuccio. Aguilar, Madrid 1969 *Seleccion, edicion y notas: Prof. Monica F. Garcia.</p></br></br>
El senado decreta erigir un altar en el campo de Marte, para celebrar el retorno victorioso de Augusto, de España y Galia y  por la pasificaci&oacute;n reinante en todo el Imperio. Todos los años se celebrara una ceremonia conmemorativa donde los magistrados, sacerdotes y vestales citaran el himno a la tierra fértil, aunque presente en un orden nuevo.
</br></br>[..]"El monumento no se trata de una gran obra de arte, sino de un  testimonio t&iacute;pico de su tiempo"[..]
</br></br>[..]"Ya desde el 40 a.c  Marco Antonio aparec&iacute;a como Dioniso y Octavio tomo el papel de Apolo"[..]
</br></br>[..]"En los estucos de la casa de la Farnesina, divinidades greco-egipcia (s&iacute;mbolo de la sabidur&iacute;a m&iacute;stica) hab&iacute;a tomado los rasgos de Octavio"[..]
</br></br>[..]"La espera de un soberano que trajera la paz, promotor de una nueva era, se encuentra claramente expresada a prop&oacute;sito de Augusto"[..]
</br></br>[..]"Roma y la Tierra: Los dos elementos que definieron la situaci&oacute;n del ciudadano romano bajo el r&eacute;gimen y la paz de Augusto"[..]
</br></br>[..]"El arte bajo Augusto, se estableci&oacute; sobre una ficci&oacute;n: La continuidad de la tradici&oacute;n de los reinos helenísticos y al mismo tiempo, de la rep&uacute;blica consular romana; Este arte teme expresar el sentimiento y se refugia en el conformismo de la correcci&oacute;n y del virtuosismo t&eacute;cnico"[..]
</br></br>[..]"Uni&oacute;n de temas m&iacute;sticos y de temas hist&oacute;ricos, y la indiferencia hacia la l&oacute;gica estructural de las diversas partes, representan elementos de concepci&oacute;n y de gustos romanos, ligados a la tradici&oacute;n it&aacute;lica"[..] 
</br></br> En el monumento podemos observar a la familia de Augusto, junto con im&aacute;genes de diosas y los mismos fundadores 
de Roma  (R&oacute;mulo y Remo).  Disfraz literario y retorico.
</br></br>[..]"Las preocupaciones de forma no dominan, pero la narraci&oacute;n viva y f&aacute;cilmente comprensible prevalece. Arte plebeyo, lo representado debe tener clara legibilidad para el espectador/lector y por tanto se comienza a estructurar la composici&oacute;n en dos planos: primer plano para aquello que se quiere destacar; segundo plano, para aquello que acompaña al motivo principal"[..]  
</br></br>La relaci&oacute;n entre el exterior e interior de los motivos representados en el monumento Ara Pacis. Por un lado encontramos serie de bajorrelieves que narran los comienzos de Roma, con una fuerte orientaci&oacute;n pol&iacute;tica, y por el otro, sutiles elementos decorativos que tratan sobre la realizaci&oacute;n de la ceremonia funcional al monumento. 
</br></br>[..]"En el Ara Pacis, Pergamo, Alejandr&iacute;a , Atenas, hacen confluir su herencia art&iacute;stica en una obra que se muestra como t&iacute;pica del siglo de Augusto"[..]
