<div id="anali_1" class="anali_to">
<h3>Mosaico - Santa Sofía de Constantinopla - Jesús y el Papa León VI. -Siglo X</h3></br>
<h3>Santa Sofía de Constantinopla (Estambul), construida por orden de Justiniano en el siglo VI, la más brillante expresión técnica - 
	con la enorme innovación de su cúpula esférica central - y estética de una arquitectura explícitamente dedicada a manifestar el 
	poderío totalizador del cristianismo y del cuasi-divinizado emperador.</h3></br>
       <p>Rafael Argullol  - Tres miradas sobre el arte. (p.131).</p>
<h3>Propias de una visión naturalista - y propugnaba la representación plana y lineal. De ahí la preferencia bizantina por el mosaico, 
	además de facilitar este tipo de representación, permite el más amplio decorativismo. El mosaico es el espacio mas genuino de la figuración bizantina.</h3>
       <p>Rafael Argullol  - Tres miradas sobre el arte. (p.131).</p>
</div>

<div id="anali_2" class="anali_to">
<h3>Se encuentra en el tímpano sobre la puerta imperial. Esta puerta fue utilizada solamente por los emperadores, 
	y fue la entrada de mayor esplendor a la iglesia. Los mosaicos se remontan al siglo IX o X.</h3>
<h3>El emperador León VI con un halo sobre la cabeza está dando Proskynesis, un acto de respeto - a Cristo, que está sentado en un trono enjoyado. 
	Con su mano derecha, Cristo está dando bendición al emperador, y su mano izquierda sostiene un libro escrito " La paz sea con vosotros. 
	Yo soy la luz del mundo".</h3>
<h3>A ambos lados de Cristo, hay figuras en redondeles. Uno de ellos es un Madre María y la otra es el Arcángel Gabriel. </h3>

</div>


<div id="anali_3" class="anali_to">
<h3>Esta imagen tiene la intención de mostrar el poder eterno del emperador y sus súbditos la bendición de Cristo.</h3>
<h3>No importa desde que ángulo, nunca escapamos a sus ojos, Su mirada directa no será solo expresión, consecuencia,
cumplimiento de su simetría, sino también evidencia de su autoridad.</h3>
    <p>Jean Paris - El espacio y la mirada. (p.184)</p>
<h3>Imponiendo a Dios no ya como objeto para contemplar, sino como sujeto que nos contempla a nosotros.</h3>
	<p>Jean Paris - El espacio y la mirada. (p.183)</p>
<h3>Si no hay espacio detrás de los mosaicos, es porque es vano buscar allí otras profundidades que las mismas del ser cuyo silencio, 
	dice Kallistos, "es el estado supremo". El verdadero espacio, el concreto, no está situado detrás sino delante. Es el nuestro. Y , 
	por tanto, nosotros somos el cuadro. Aquí la imagen no se separa nada del que la contempla, o más bien del contemplado, por una pared ficticia, 
	como en el arte clásico, sino que se proyecta hacia él, lo engloba, lo engulle en su propio universo como si fuera un alimento.</h3>  
    <p>Jean Paris - El espacio y la mirada. (p.185)</p>
<h3>Perspectiva invertida o "negativa", las líneas convergen en un foco, nosotros. Por eso la imagen parece agrandarse a medida que nos alejamos.</h3>
    <p>Jean Paris - El espacio y la mirada. (p.187)</p>
