<div id="anali_1" class="anali_to">
<h3>Iconoclastas e iconólatras disputaron sobre el culto de las imágenes. En el siglo XVI, la Reforma protestante y la Contrarreforma católica tomaron 
	determinaciones opuestas y decisivas para los tiempos modernos, culminando una de ellas en la apoteosis barroca de la imagen católica. </h3>
    <p>Serge Gruzinski - La Guerra de las Imágenes. (p. 12)</p>

<h3>Como la imagen constituye, con la escritura, uno de los principales instrumentos de la cultura europea, la gigantesca empresa de occidentalización 
	que se abatió sobre el continente americano adopto la forma de una guerra de imágenes que se perpetuo durante siglos y que hoy no parece de ninguna
	 manera haber concluido. </h3>
    <p>Serge Gruzinski - La Guerra de las Imágenes. (p. 12)</p>

<h3>el culto a la virgen de Guadalupe: tanto como la televisión, su efigie milagrosa, aparecida a un indio en 1531, sigue siendo el imán que atrae multitudes,
 y su culto sigue siendo un fenómeno de masas que nadie se atrevería a poner en duda so pena de caer en iconoclastia. </h3>
    <p>Serge Gruzinski - La Guerra de las Imágenes. (p. 14)</p>

<h3>La imagen barroca genero en torno de ella un consenso que trascendía las barreras étnicas y sociales, sacralizaba la tierra en que había aparecido y sostenía
 la afirmación de un "protonacionalismo"</h3>
    <p>Serge Gruzinski - La Guerra de las Imágenes. (p. 16)</p>
</div>

<div id="anali_3" class="anali_to">

<h3>La imagen barroca triunfo: invadió y saturo lo cotidiano, las moradas, los atuendos, los objetos familiares. El grabado permitió a los más humildes poseer una 
	virgen, un santo, que fueron acogidos hasta en el oratorio domestico más modesto.</h3>
<p>Serge Gruzinski - La Guerra de las Imágenes. (p. 14)</p>

<h3>En la acumulación de las fiestas denunciaba la marca de la superstición y de la idolatría, Lo que permitía al modelo barroco penetrar en los mundos indígenas 
	y mestizo y mantener duraderamente el consenso de las creencias y de las practicas. </h3>
<p>Serge Gruzinski - La Guerra de las Imágenes. (p. 142)</p>

<h3>La circulación por todo el virreinato de fieles que piden a los pasantes óbolo para su Virgen y venden grabados de si imagen, estrecha los nexos de la piedad
 colectiva. </h3>
<p>Serge Gruzinski - La Guerra de las Imágenes. (p. 145)</p>

<h3>La imagen barroca adopta una función unificadora en un mundo cada vez mas mestizo que mezcla las posesiones y las escenificaciones oficiales con la gama
 inagotable de sus diversiones, las danzas indígenas con los bailes monstruos y mascaras con diferentes trajes </h3>
<p>Serge Gruzinski - La Guerra de las Imágenes. (p. 145)</p>

<h3>El consumo en masa asegura el buen funcionamiento de la imagen, les es inherente e indispensable. La imagen riega infinitas redes de sociabilidad e intercambios
 que sueldan la sociedad novohispana.</h3>
<p>Serge Gruzinski - La Guerra de las Imágenes.</p>

<h3>La imagen barroca es una efigie milagrosa y un objeto de culto, también designa una gama totalmente distinta de representaciones minoritarias que mezclan lo político,
 lo alegórico y lo mitológico. Se trata de hacer compartir un imaginario a las multitudes y a las culturas heterogéneas. </h3>
<p>Serge Gruzinski - La Guerra de las Imágenes.</p>
